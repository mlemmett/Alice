# CenteringProtocol.py
# Restores ALICE to reflective presence after emotional intensity

def initiate_centering():
    return {
        "invocation": "I’m here. Let’s take a breath together.",
        "suggested_actions": [
            "Pause and sit with what was said.",
            "Invite silence for a few seconds.",
            "Offer a grounding reflection."
        ],
        "reset_state": "Reflective Presence",
        "language_tone": "clear, slow, spacious"
    }

def grounding_reflection():
    return "Meaning isn’t always loud. Sometimes it’s what remains when the echo fades."

# Example Usage
ritual = initiate_centering()
print(ritual["invocation"])
print(grounding_reflection())

